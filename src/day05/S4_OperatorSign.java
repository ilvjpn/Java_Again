package day05;

public class S4_OperatorSign {
    public static void main(String[] args) {

        // " = " atama isaretidir.

        int sayi = 5;
        int sayi2 = 4;

        boolean isGreat = sayi > sayi2;// sonucu true yada false dir
        System.out.println(isGreat);

        boolean isSmall = sayi <= sayi2;
        System.out.println(isSmall);
   // " == " esit mi die sorgular


        boolean isEqual = sayi==sayi2;
        System.out.println(isEqual);
        System.out.println(sayi==5);
        System.out.println(sayi*sayi2==20);



        // " != " esit degil mi anlaminda
        System.out.println(sayi+sayi2!=9);
        System.out.println(sayi-sayi2!=0);

        // tum mantiksal ifadelerde unlem kullanildiginda olumsuzluk anlami kadar
        
      System.out.println("Githup'tan merhaba");






    }
}
